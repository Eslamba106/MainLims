سياسة الخصوصية
يدرك موقع (منصة الكيمياء) أهمية خصوصية وحماية بياناتك، وبالتالي نلتزم بالحفاظ على سرية المعلومات الحساسة وحماية حقوق جميع زوار هذا الموقع، وقد أعددنا هذه السياسة وبشكل محدد لتوضيح جميع ما يلي:

الموافقة على سياسة الخصوصية

استخدامك للموقع وتطبيقه يعني موافقتك على سياسة الخصوصية هذه، والضوابط والشروط التي تحكم الموقع والتطبيق. فإذا لم تكن موافقاً على هذه السياسة يجب عليك عدم استخدام أي من الخدمات المقدمة من خلال موقع (منصة الكيمياء).



من يقوم بجمع بيانات المستخدمين؟

بمجرد زيارتك للموقع chemstage.com ، يقوم الخادم الخاص بالموقع الذي يعمل بشكل تلقائي، بتسجيل عنوان بروتوكول شبكة الإنترنت IP الخاص بك (عنوان بروتوكول شبكة الإنترنت الخاص بك هو الرقم الخاص بجهاز الكمبيوتر الذي تستخدمه، والذي يسمح للأجهزة الأخرى الموصولة بشبكة الإنترنت بتحديد وجهة البيانات الصادرة عنها، وجمع بعض المعلومات مثل نوع المتصفح ومحرك البحث، وتاريخ ووقت الزيارة والعنوان URL الخاص بأي موقع إلكتروني تتم منه إحالتك إلى موقع chemstage.com، ولكن من دون التعرف على هويتك الشخصية).



الروابط بالمواقع الأخرى والأطراف الثالثة على شبكة الإنترنت

ينطبق نهج الخصوصية هذا على موقع (منصة الكيمياء) فقط. وإذا ما انتقلت إلى موقع آخر من خلال (منصة الكيمياء)، يتعين عليك قراءة نهج الخصوصية الخاص بذلك الموقع؛ لتحديد ممارسات أمن المعلومات وسياسة الخصوصية المعتمدة هناك.



ما هي البيانات التي يتم جمعها؟

1- ملفات تعريف الارتباط (Cookies)

قد يقوم الموقع بتخزين ما يسمى بملفات تعريف الارتباط “Cookies” على الكمبيوتر الخاص بك عندما تقوم بزيارة الموقع. إن ملفات تعريف الارتباط “Cookies” هي جزء من البيانات التي تحددك كمستخدم بشكل فريد، كما يمكن استخدامها لتطوير معرفتك بالموقع ولفهم قاعدة مستخدم هذا الموقع على نحو أفضل.

يتم إعداد معظم المتصفحات في البداية لقبول ملفات تعريف الارتباط، كما يمكنك إعادة إعداد المتصفح الخاص بك لرفض كافة ملفات تعريف الارتباط أو للتنبيه عندما يتم إرسال ملفات تعريف الارتباط. كما يرجى ملاحظة أن بعض الخدمات التي يتم عرضها على هذا الموقع قد لا تعمل بشكل صحيح إذا قمت برفض ملفات تعريف الارتباط، حيث أننا قد نقوم / لا نقوم بربط المعلومات التي نقوم بتخزينها في ملفات تعريف الارتباط بأي معلومات تعريف شخصية تقدمها على موقعنا.

2- المعلومات الشخصية

يلتزم موقع (منصة الكيمياء) بالمبادئ الرئيسية والقواعد العامة لحماية البيانات الشخصية، وكذلك المبادئ الرئيسية والقواعد العامة لمشاركة البيانات.

3- الموقع الجغرافي

قد يُطلب من المستخدم في بعض الأحيان الموافقة على تحديد الموقع الجغرافي له، للاستفادة من بعض الخدمات المقدمة في (منصة الكيمياء) وتطبيقها.



ما هو الغرض من جمع البيانات؟

استخدام الموقع لبروتوكول الإنترنت الخاص بك يساعد على تشخيص المشكلات التي تحدث في الخوادم الخاصة بها، ويساعدها أيضًا على إجراء الإحصاء اللازم لقياس استخدام الموقع (عدد الزوار ولغة جهاز الكمبيوتر الذي تستخدمه).



انتقال آمن للمعلومات

نحن نضمن لك بأن هذا الموقع يحوز على أعلى إجراءات الأمن لحماية المعلومات التي تقدمها له. الموقع يعمل على تشفير المعلومات التي تعتبر حساسة (مثل أرقام بطاقات الائتمان) أو البيانات التي يتعين الحفاظ على سريتها توافقاً مع المتطلبات القانونية.



حماية المعلومات الشخصية

لن تكون معلوماتك الشخصية متاحة إلا لموظفي الموقع الذين يحتاجون إلى الاطلاع عليها. ودون موافقة المستخدم، لن تكون تلك المعلومات متاحة للجمهور. وعلاوة على ذلك لن يتم تبادل، أو تداول، أي من تلك المعلومات، أو بيعها لأي طرف آخر من غير الموافقة المسبقة للمستخدم، كما لن يسمح بالوصول إلى المعلومات إلا للأشخاص المؤهلين والذين يتولون تقديم خدمات (منصة الكيمياء).



حقوق المستخدم

يحق لأي مستخدم يزود الموقع بمعلوماته الشخصية التالي:

الوصول إلى المعلومات.

الحصول على المعلومات.

مراجعة وتعديل المعلومات.

سحب الموافقة وحذف المعلومات.

وذلك عبر إرسال رسالة إلكترونية إلى care@chemstage.com .

